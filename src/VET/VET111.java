package VET;

public class VET111 {

	private String nome;
	private String gmail;
	
	public  VET111(String nome, String gmail) { // lembra de deixar sem o void
		
		this.nome = nome;
		this.gmail = gmail;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getGmail() {
		return gmail;
	}

	public void setGmail(String gmail) {
		this.gmail = gmail;
	}
	
}
